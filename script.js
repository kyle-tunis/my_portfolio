
// Saving homepage innerHTML for use on button1click

const saveDate = document.getElementById('date').innerHTML;
const saveName = document.getElementById('project-name').innerHTML;
const saveDescription = document.getElementById('project-description').innerHTML;
const saveImg = document.getElementById('projectimg').innerHTML;



//Project 1 Button Event 
const proj1but = document.getElementById('proj1but');

function button1(event) {
    document.getElementById('date').innerHTML = saveDate;
    document.getElementById('project-name').innerHTML = saveName;
    document.getElementById('project-description').innerHTML = saveDescription;
    document.getElementById('projectimg').innerHTML = saveImg;

};

proj1but.onclick = button1;


